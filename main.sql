-- Câu 6: Hiển thị MaDV, TenDV, DonViTinh, DonGia của những dịch vụ đi kèm có DonViTinh là “lon” và có DonGia
-- lớn hơn 10,000 VNĐ hoặc những dịch vụ đi kèm có DonViTinh là “Cai” và có DonGia nhỏ hơn 5,000 VNĐ. (1 điểm)
SELECT MADV,
       TENDV,
       DONVITINH,
       DONGIA
FROM DICH_VU_DI_KEM
WHERE (DONVITINH = 'lon' AND DONGIA > 10.000)
   OR (DONVITINH = 'Cai' AND DONGIA < 5.000);


-- Câu 7: Hiển thị MaDatPhong, MaPhong, LoaiPhong, SoKhachToiDa, GiaPhong, MaKH, TenKH, SoDT, NgayDat, GioBatDau,
-- GioKetThuc, MaDichVu, SoLuong, DonGia của những đơn đặt phòng có năm đặt phòng là “2016”, “2017” và đặt
-- những phòng có giá phòng > 50,000 VNĐ/ 1 giờ. (1 điểm)
SELECT
    CHI_TIET_SU_DUNG_DV.MADATPHONG,
    PHONG.MAPHONG,
    PHONG.LOAIPHONG,
    PHONG.SOKHACHTOIDA,
    PHONG.GIAPHONG,
    KHACH_HANG.MAKH,
    KHACH_HANG.TENKH,
    KHACH_HANG.SODT,
    DAT_PHONG.NGAYDAT,
    DAT_PHONG.GIOBATDAU,
    DAT_PHONG.GIOKETTHUC,
    DICH_VU_DI_KEM.MADV,
    CHI_TIET_SU_DUNG_DV.SOLUONG,
    DICH_VU_DI_KEM.DONGIA
FROM
    DAT_PHONG
JOIN
    PHONG ON DAT_PHONG.MAPHONG = PHONG.MAPHONG
JOIN
    KHACH_HANG ON DAT_PHONG.MAKH = KHACH_HANG.MAKH
JOIN
    CHI_TIET_SU_DUNG_DV ON DAT_PHONG.MADATPHONG = CHI_TIET_SU_DUNG_DV.MADATPHONG
JOIN
    DICH_VU_DI_KEM ON CHI_TIET_SU_DUNG_DV.MADV = DICH_VU_DI_KEM.MADV
WHERE
    (EXTRACT(YEAR FROM DAT_PHONG.NGAYDAT) = 2016 OR EXTRACT(YEAR FROM DAT_PHONG.NGAYDAT) = 2017)
    AND PHONG.GIAPHONG > 50.000;


-- Câu 8: Hiển thị MaDatPhong, MaPhong, LoaiPhong, GiaPhong, TenKH, NgayDat, TongTienHat, TongTienSuDungDichVu,
-- TongTienThanhToan tương ứng với từng mã đặt phòng có trong bảng DAT_PHONG. Những đơn đặt phòng nào không sử dụng
-- dịch vụ đi kèm thì cũng liệt kê thông tin của đơn đặt phòng đó ra. (1 điểm)
SELECT
    DAT_PHONG.MADATPHONG,
    DAT_PHONG.MAPHONG,
    PHONG.LOAIPHONG,
    PHONG.GIAPHONG,
    KHACH_HANG.TENKH,
    DAT_PHONG.NGAYDAT,
    -- TongTienHat = GiaPhong * (GioKetThuc – GioBatDau)
    (PHONG.GIAPHONG * EXTRACT(EPOCH FROM (DAT_PHONG.GIOKETTHUC - DAT_PHONG.GIOBATDAU)) / 3600) AS TONGTIENHAT,
    -- TongTienSuDungDichVu = SoLuong * DonGia
    COALESCE(SUM(CHI_TIET_SU_DUNG_DV.SOLUONG * DICH_VU_DI_KEM.DONGIA), 0) AS TONGTIENSUDUNGDICHVU,
    -- TongTienThanhToan = TongTienHat + sum(TongTienSuDungDichVu)
    (PHONG.GIAPHONG * EXTRACT(EPOCH FROM (DAT_PHONG.GIOKETTHUC - DAT_PHONG.GIOBATDAU)) / 3600)
        + COALESCE(SUM(CHI_TIET_SU_DUNG_DV.SOLUONG * DICH_VU_DI_KEM.DONGIA), 0) AS TONGTIENTHANHTOAN
FROM
    DAT_PHONG
JOIN
    PHONG ON DAT_PHONG.MAPHONG = PHONG.MAPHONG
JOIN
    KHACH_HANG ON DAT_PHONG.MAKH = KHACH_HANG.MAKH
LEFT JOIN
    CHI_TIET_SU_DUNG_DV ON DAT_PHONG.MADATPHONG = CHI_TIET_SU_DUNG_DV.MADATPHONG
LEFT JOIN
    DICH_VU_DI_KEM ON CHI_TIET_SU_DUNG_DV.MADV = DICH_VU_DI_KEM.MADV
GROUP BY
    DAT_PHONG.MADATPHONG, DAT_PHONG.MAPHONG, PHONG.LOAIPHONG, PHONG.GIAPHONG,
    KHACH_HANG.TENKH, DAT_PHONG.NGAYDAT, DAT_PHONG.GIOBATDAU, DAT_PHONG.GIOKETTHUC
ORDER BY
    DAT_PHONG.MADATPHONG;