-- Câu 4: Hiển thị TenKH của những khách hàng có tên bắt đầu là một trong các ký tự “H”, “N”, “M”
-- và có độ dài tối đa là 20 ký tự. (1 điểm)
SELECT TENKH
FROM KHACH_HANG
WHERE TENKH LIKE 'H%' OR TENKH LIKE 'N%' OR TENKH LIKE 'M%'
AND LENGTH(TENKH) <= 20;


